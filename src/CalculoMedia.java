public class CalculoMedia {
    public static void main(String[] args) {
        double Atividade1 = 10;
        double Atividade2 = 20;
        double Atividade3 = 5.5;

        double media = (Atividade1 + Atividade2 + Atividade3) / 3;

        System.out.println("A média é: " + media);
    }
}

